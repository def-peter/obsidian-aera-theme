import { mkdir, writeFile } from "node:fs/promises";

const revision = "62ecdc0d7ca5c6df32148c169556bc8d3782fca4";
const outputUrl = new URL("../src/generated/_fluent-emoji.scss", import.meta.url);
const assets = [
  ["feather", "Feather"],
  ["open-book", "Open book"],
  ["information", "Information"],
  ["check-box-with-check", "Check box with check"],
  ["sparkles", "Sparkles"],
  ["seedling", "Seedling"],
  ["red-question-mark", "Red question mark"],
  ["warning", "Warning"],
  ["prohibited", "Prohibited"],
  ["collision", "Collision"],
  ["lady-beetle", "Lady beetle"],
  ["card-index-dividers", "Card index dividers"],
  ["speech-balloon", "Speech balloon"],
];

function assetUrl(directory) {
  const filename = `${directory.toLowerCase().replaceAll(" ", "_")}_flat.svg`;
  const path = ["assets", directory, "Flat", filename]
    .map(encodeURIComponent)
    .join("/");

  return `https://raw.githubusercontent.com/microsoft/fluentui-emoji/${revision}/${path}`;
}

async function downloadAsset([name, directory]) {
  const response = await fetch(assetUrl(directory));
  if (!response.ok) {
    throw new Error(`Unable to download ${directory}: HTTP ${response.status}`);
  }

  const svg = await response.text();
  if (!/^<svg\b/u.test(svg.trim())) {
    throw new Error(`Downloaded ${directory} asset is not an SVG`);
  }

  return [name, Buffer.from(svg).toString("base64")];
}

const encodedAssets = await Promise.all(assets.map(downloadAsset));
const entries = encodedAssets
  .map(
    ([name, data]) =>
      `  "${name}": "data:image/svg+xml;base64,${data}",`,
  )
